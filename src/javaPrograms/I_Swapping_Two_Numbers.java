package javaPrograms;

public class I_Swapping_Two_Numbers {

	public static void main(String[] args) {
		
		//Declaring variables
		int a = 9;
		int b = 99;
		
		int temp = 0;
		
		temp = a;
		
		a = b;
		
		b= temp;
		
		//Printing a and b values
		System.out.println(" 'a' value is: " + a);
		System.out.println(" 'b' value is: " + b);

	}

}
